
public class Punto
{
    private double x;
    private double y;
    public Punto( double x, double y )
    {
        this.x = x;
        this.y = y;
    }

    public double getX( )
    {
        return x;
    }

    public double getY( )
    {
        return y;
    }

    public void setX( double pX )
    {
        x = pX;
    }

    public void setY( double pY )
    {
        y = pY;
    }

}